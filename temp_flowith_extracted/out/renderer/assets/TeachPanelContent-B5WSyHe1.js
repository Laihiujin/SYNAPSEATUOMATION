function formatUrl(url) {
  if (!url) return "—";
  const INTERNAL_PAGES = ["flowith://", "about:", "chrome://", "file://"];
  if (INTERNAL_PAGES.some((p2) => url.startsWith(p2))) {
    return "—";
  }
  try {
    const parsed = new URL(url);
    return `${parsed.hostname}${parsed.pathname === "/" ? "" : parsed.pathname}`;
  } catch {
    return url;
  }
}
